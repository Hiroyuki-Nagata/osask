#include <cstddef>

//=============================================================================
// compare SZ bytes of D and S
//=============================================================================
int GO_memcmp (void *d, void *s, size_t sz)
{
	char *dp = (char*)d;
	char *sp = (char*)s;

	while (sz--) {
		if (*dp != *sp)
			return *dp - *sp;
		dp++;
		sp++;
	}

	return 0;
}
